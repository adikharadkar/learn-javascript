// Program to convert a string into int and float

let num = "21";
let dec = "30.5";

// String to Integer
console.log(parseInt(num));

// String to Float
console.log(parseFloat(dec));

console.log(typeof parseInt(num));
console.log(typeof parseFloat(dec));
console.log(num, dec);

