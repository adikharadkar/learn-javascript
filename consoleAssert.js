// console.assert() method asserts a condition and if the condition is false, then it outputs a message in the console.
// If the condition is true then nothing happens.

console.assert(25 == 25, "25 is equal to 25"); // Output will not print in the console as the condition is TRUE
console.assert(25 === "25", "25 is not equal to '25'"); // Output will print in the console as the condition is FALSE
